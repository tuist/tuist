#!/usr/bin/env bash
#MISE description="Detect whether the current branch has releasable changes"
set -euo pipefail

latest_version="$(git tag -l | grep -E '^[0-9]+\.[0-9]+\.[0-9]+$' | sort -V | tail -n1 || true)"

if [[ -n "${latest_version}" ]]; then
  changelog="$(git cliff --config cliff.toml --repository . -- "${latest_version}..HEAD" 2>/dev/null || true)"
else
  changelog="$(git cliff --config cliff.toml --repository . 2>/dev/null || true)"
fi

notes_section="$(printf '%s' "${changelog}" | sed -n '/<!-- RELEASE NOTES START -->/,/<!-- generated by git-cliff -->/p')"
should_release=false
if printf '%s' "${notes_section}" | grep -qE '^\*'; then
  should_release=true
fi

if [[ "${should_release}" == true ]]; then
  if [[ -n "${latest_version}" ]]; then
    next_version="$(git cliff --config cliff.toml --repository . --bumped-version -- "${latest_version}..HEAD")"
  else
    next_version="$(git cliff --config cliff.toml --repository . --bumped-version)"
  fi
else
  next_version="${latest_version:-0.1.0}"
fi

echo "latest-version=${latest_version:-none}"
echo "next-version=${next_version}"
echo "should-release=${should_release}"

if [[ -n "${GITHUB_OUTPUT:-}" ]]; then
  {
    echo "latest-version=${latest_version}"
    echo "next-version=${next_version}"
    echo "should-release=${should_release}"
  } >> "${GITHUB_OUTPUT}"
fi
