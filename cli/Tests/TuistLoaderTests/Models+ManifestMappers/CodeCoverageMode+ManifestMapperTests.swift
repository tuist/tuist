import FileSystem
import FileSystemTesting
import Foundation
import ProjectDescription
import Testing
import TuistCore
import TuistSupport
import XcodeGraph

@testable import TuistLoader
@testable import TuistTesting

struct CodeCoverageManifestMapperTests {
    private typealias Manifest = ProjectDescription.Workspace.GenerationOptions.AutogeneratedWorkspaceSchemes.CodeCoverageMode

    @Test(.inTemporaryDirectory) func from_returnsTheCorrectValue_whenManifestIsAll() throws {
        // Given
        let temporaryPath = try #require(FileSystem.temporaryTestDirectory)
        let generatorPaths = GeneratorPaths(
            manifestDirectory: temporaryPath,
            rootDirectory: temporaryPath
        )
        let manifest = Manifest.all

        // When
        let got = try XcodeGraph.Workspace.GenerationOptions.AutogeneratedWorkspaceSchemes.CodeCoverageMode
            .from(manifest: manifest, generatorPaths: generatorPaths)

        // Then
        #expect(.all == got)
    }

    @Test(.inTemporaryDirectory) func from_returnsTheCorrectValue_whenManifestIsRelevant() throws {
        // Given
        let temporaryPath = try #require(FileSystem.temporaryTestDirectory)
        let generatorPaths = GeneratorPaths(
            manifestDirectory: temporaryPath,
            rootDirectory: temporaryPath
        )
        let manifest = Manifest.relevant

        // When
        let got = try XcodeGraph.Workspace.GenerationOptions.AutogeneratedWorkspaceSchemes.CodeCoverageMode
            .from(manifest: manifest, generatorPaths: generatorPaths)

        // Then
        #expect(.relevant == got)
    }

    @Test(.inTemporaryDirectory) func from_returnsTheCorrectValue_whenManifestIsTargets() throws {
        // Given
        let temporaryPath = try #require(FileSystem.temporaryTestDirectory)
        let generatorPaths = GeneratorPaths(
            manifestDirectory: temporaryPath,
            rootDirectory: temporaryPath
        )
        let targetRef = ProjectDescription.TargetReference.target("Target")
        let manifest = Manifest.targets([targetRef])

        // When
        let got = try XcodeGraph.Workspace.GenerationOptions.AutogeneratedWorkspaceSchemes.CodeCoverageMode
            .from(manifest: manifest, generatorPaths: generatorPaths)

        // Then
        #expect(
            got ==
            .targets([
                TargetReference(
                    projectPath: try generatorPaths.resolveSchemeActionProjectPath(nil),
                    name: "Target"
                ),
            ])
        )
    }
}
