import FileSystem
import FileSystemTesting
import Foundation
import ProjectDescription
import Testing
import TuistCore
import TuistSupport
import TuistTesting
import XcodeGraph

@testable import TuistLoader

struct WorkspaceGenerationOptionsManifestMapperTests {
    @Test(.inTemporaryDirectory) func from_whenAutomaticXcodeSchemeIsDefault() throws {
        // Given
        let temporaryPath = try #require(FileSystem.temporaryTestDirectory)
        let generatorPaths = GeneratorPaths(
            manifestDirectory: temporaryPath,
            rootDirectory: temporaryPath
        )
        let manifest = ProjectDescription.Workspace.GenerationOptions.options(
            enableAutomaticXcodeSchemes: nil,
            autogeneratedWorkspaceSchemes: .disabled,
            lastXcodeUpgradeCheck: .init("1.2.3"),
            renderMarkdownReadme: false
        )

        // When
        let actual = try XcodeGraph.Workspace.GenerationOptions.from(manifest: manifest, generatorPaths: generatorPaths)

        // Then
        #expect(
            actual ==
            .init(
                enableAutomaticXcodeSchemes: nil,
                autogeneratedWorkspaceSchemes: .disabled,
                lastXcodeUpgradeCheck: .init("1.2.3"),
                renderMarkdownReadme: false
            )
        )
    }

    @Test(.inTemporaryDirectory) func from_whenAutomaticXcodeSchemeIsDisabled() throws {
        // Given
        let temporaryPath = try #require(FileSystem.temporaryTestDirectory)
        let generatorPaths = GeneratorPaths(
            manifestDirectory: temporaryPath,
            rootDirectory: temporaryPath
        )
        let manifest = ProjectDescription.Workspace.GenerationOptions.options(
            enableAutomaticXcodeSchemes: false,
            autogeneratedWorkspaceSchemes: .disabled
        )

        // When
        let actual = try XcodeGraph.Workspace.GenerationOptions.from(manifest: manifest, generatorPaths: generatorPaths)

        // Then
        #expect(
            actual ==
            .init(
                enableAutomaticXcodeSchemes: false,
                autogeneratedWorkspaceSchemes: .disabled,
                lastXcodeUpgradeCheck: nil,
                renderMarkdownReadme: false
            )
        )
    }

    @Test(.inTemporaryDirectory) func from_whenAutomaticXcodeSchemeIsEnabled() throws {
        // Given
        let temporaryPath = try #require(FileSystem.temporaryTestDirectory)
        let generatorPaths = GeneratorPaths(
            manifestDirectory: temporaryPath,
