import Foundation
import ProjectDescription
import TuistCore
import TuistGraph
import TuistSupport
import TuistSupportTesting
import XCTest

@testable import TuistLoader
@testable import TuistSupportTesting

final class TargetScriptManifestMapperTests: TuistUnitTestCase {
    private var fileSystem: FileSysteming!

    override func setUp() {
        super.setUp()
        fileSystem = FileSystem()
    }

    override func tearDown() {
        fileSystem = nil
        super.tearDown()
    }

    func test_outputPaths_arePreserved_whenNestedFileDoesNotExistAtGenerateTime() async throws {
        let temporaryPath = try temporaryPath()
        let rootDirectory = temporaryPath
        let generatorPaths = GeneratorPaths(
            manifestDirectory: temporaryPath,
            rootDirectory: rootDirectory
        )

        // Regression Test for #5925:
        let nestedOutputPath = "Generated/Build/AutoGenerated.swift"
        
        let manifest = ProjectDescription.TargetScript.test(
            name: "GenerateFile",
            tool: "my_tool",
            order: .pre,
            arguments: [],
            outputPaths: [nestedOutputPath]
        )
        let model = try await XcodeGraph.TargetScript.from(
            manifest: manifest,
            generatorPaths: generatorPaths,
            fileSystem: fileSystem
        )
        XCTAssertEqual(model.outputPaths, [nestedOutputPath])
    }
}
