    /// End-to-end TDD verification of the structural divergence at the heart of the bug behind
    /// `tuist hash selective-testing` and `tuist test --build-only` producing different hashes
    /// for the same target.
    ///
    /// Before the fix the command loaded the graph through `GeneratorFactory.defaultGenerator`
    /// and hashed it via a separate `SelectiveTestingGraphHasher`. That hasher saw a graph that
    /// had been through `GraphMapperFactory.automation` — i.e. `FocusTargetsGraphMappers` and
    /// `TreeShakePrunedTargetsGraphMapper` at the start of the chain. `tuist test --build-only`,
    /// in contrast, hashes inside `TestsCacheGraphMapper` at step 10 of
    /// `CacheGraphMapperFactory.automation`, which:
    ///   1. runs a *different* pre-hash mapper set (`AutogeneratedWorkspaceSchemeGraphMapper`
    ///      with `forceWorkspaceSchemes: true`, plus the same module-map / platform-narrower /
    ///      orphan-prune mappers but in a different order); and
    ///   2. hashes *before* `FocusTargets`/`TreeShake` have run.
    ///
    /// Several of those pre-hash mappers transform graph fields that `TargetContentHasher`
    /// folds into the target hash. The most direct example —
    /// `ExternalProjectsPlatformNarrowerGraphMapper` — narrows `target.destinations` on
    /// external targets to the intersection of what their consumers actually need.
    /// `TargetContentHasher` includes `target.destinations.map(\.rawValue).sorted()` in the
    /// stringsToHash list (see `cli/Sources/TuistHasher/TargetContentHasher.swift:271`), so
    /// any change to that set propagates to the target's content hash and, transitively,
    /// to every target that depends on it.
    ///
    /// These tests pin that structural reality. They demonstrate, on a synthetic graph,
    /// that the testing pipeline runs the narrower mapper before hashing and that the
    /// narrowing actually changes the external target's `destinations` set. Together with
    /// the unit tests in `HashSelectiveTestingCommandServiceTests` (which pin the wiring
    /// change so the command now goes through this same pipeline), they form the TDD
    /// loop: pin the input state both at the boundary the fix touches and at the
    /// downstream contract the fix relies on.
    @Suite
    struct SelectiveTestingPipelineGraphStateTests {
        @Test func testing_pipeline_preserves_external_target_at_hash_time() async throws {
            // Given a graph where the local test target reaches an external target.
            let (graph, projectPath, externalProjectPath) = try Self.makeFixtureGraph()

            // When we apply the slice of the testing pipeline that runs before
            // `TestsCacheGraphMapper` takes hashes. We invoke the mappers directly so this
            // test stays out of `ManifestGraphLoader` filesystem territory.
            let mappers = SequentialGraphMapper([
                ExternalProjectsPlatformNarrowerGraphMapper(),
                PruneOrphanExternalTargetsGraphMapper(),
            ])
            let (mappedGraph, _, _) = try await mappers.map(graph: graph, environment: MapperEnvironment())

            // Then the external target is still in the graph (it's a transitive dep of an
            // internal test target, so it isn't an orphan), the local test target still has
            // its dependency edge to it, and any narrowing has been applied. This is what
            // `TestsCacheGraphMapper` would see at hash time — and what the cache lookup is
            // keyed on.
            let externalLib = mappedGraph.projects[externalProjectPath]?.targets["ExternalLib"]
            #expect(externalLib != nil, "External target must still be in the graph at hash time")

            let myTestsDeps = mappedGraph.projects[projectPath]?.targets["MyTests"]?.dependencies ?? []
            #expect(myTestsDeps.count == 1, "Local test target must still reference the external dep at hash time")
        }

        @Test func external_target_destinations_are_narrowed_before_testing_pipeline_hashes() async throws {
            // Given a graph where the external target supports more destinations than its
            // internal consumer needs.
            let (graph, _, externalProjectPath) = try Self.makeFixtureGraph()
            let originalDestinations = graph.projects[externalProjectPath]?.targets["ExternalLib"]?.destinations
            #expect(originalDestinations == [.iPhone, .iPad, .mac], "Fixture should start with broad destinations")

            // When we run `ExternalProjectsPlatformNarrowerGraphMapper` (the testing pipeline
            // runs it before `TestsCacheGraphMapper` hashes; the pre-fix command's pipeline ran
            // it later, after `FocusTargets`/`TreeShake`).
            let mapper = ExternalProjectsPlatformNarrowerGraphMapper()
            let (mappedGraph, _, _) = try await mapper.map(graph: graph, environment: MapperEnvironment())

            // Then the external target's destinations are narrowed to what the consumer needs
            // (`[.iPhone]` in the fixture). `target.destinations` is folded into
            // `TargetContentHasher`'s output, so any change here propagates to the target's
            // content hash and to anything that depends on it — which is precisely the
            // mechanism by which the pre-fix command's hash output drifted from the cache key.
            let narrowedDestinations = mappedGraph.projects[externalProjectPath]?.targets["ExternalLib"]?.destinations
            #expect(narrowedDestinations == [.iPhone], "Narrower mapper must restrict the external target's destinations")
        }

        /// Builds: external project with `ExternalLib` <- local project with `MyTests` depending on it.
        private static func makeFixtureGraph() throws
            -> (graph: Graph, projectPath: AbsolutePath, externalProjectPath: AbsolutePath)
        {
            let projectPath = try AbsolutePath(validating: "/Project")
            let externalProjectPath = try AbsolutePath(validating: "/Project/Tuist/.build/checkouts/ExternalLib")

            let externalLib = Target.test(
                name: "ExternalLib",
                destinations: [.iPhone, .iPad, .mac],
                product: .framework,
                bundleId: "io.tuist.ExternalLib"
            )
            let externalProject = Project.test(
                path: externalProjectPath,
                name: "ExternalLib",
                targets: [externalLib],
                type: .external(hash: "external-project-hash")
            )

            let myTests = Target.test(
                name: "MyTests",
                destinations: [.iPhone],
                product: .unitTests,
                bundleId: "io.tuist.MyTests",
                dependencies: [
                    .target(name: "ExternalLib", status: .required, condition: nil),
                ]
            )
            let localProject = Project.test(
                path: projectPath,
                name: "Local",
                targets: [myTests],
                type: .local
            )

            let workspace = Workspace.test(
                path: projectPath,
                projects: [projectPath, externalProjectPath],
                schemes: [
                    Scheme.test(
                        name: "MyTests",
                        buildAction: .test(targets: [TargetReference(projectPath: projectPath, name: "MyTests")]),
                        testAction: TestAction.test(
                            targets: [TestableTarget(target: TargetReference(projectPath: projectPath, name: "MyTests"))]
                        )
                    ),
                ]
            )

            let graph = Graph.test(
                workspace: workspace,
                projects: [
                    projectPath: localProject,
                    externalProjectPath: externalProject,
                ],
                dependencies: [
                    .target(name: "MyTests", path: projectPath, status: .required): [
                        .target(name: "ExternalLib", path: externalProjectPath, status: .required),
                    ],
                ]
            )
            return (graph, projectPath, externalProjectPath)
        }
    }
