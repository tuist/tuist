        try await assertLoadingRaisesWhenManifestNotFound(manifest: .plugin)
    }

    @Test(.inTemporaryDirectory) func run_throws_when_the_manifest_loading_fails() async throws {
        for manifest in DumpableManifest.allCases {
            let tmpDir = try #require(FileSystem.temporaryTestDirectory)
            try "invalid config".write(
                toFile: tmpDir.appending(component: manifest.manifest.fileName(tmpDir)).pathString,
                atomically: true,
                encoding: .utf8
            )
            do {
                try await subject.run(path: tmpDir.pathString, manifest: manifest)
                Issue.record("Expected error not thrown")
            } catch {
                // can't use XCTAssertError because it doesn't support async
            }
        }
    }

    // MARK: - Helpers

    private func assertLoadingRaisesWhenManifestNotFound(manifest: DumpableManifest) async throws {
        try await fileHandler.inTemporaryDirectory { tmpDir in
            var expectedDirectory = tmpDir
            if manifest == .config {
                if try await !fileSystem.exists(expectedDirectory) {
                    try await fileSystem.makeDirectory(at: expectedDirectory)
                }
            }
            await #expect(throws: ManifestLoaderError.manifestNotFound(manifest.manifest, expectedDirectory)) {
                try await subject.run(path: tmpDir.pathString, manifest: manifest)
            }
        }
    }
}

extension DumpableManifest {
    var manifest: Manifest {
        switch self {
        case .project:
            return .project
        case .workspace:
            return .workspace
        case .config:
            return .config
        case .template:
            return .template
        case .plugin:
            return .plugin
        case .package:
            return .packageSettings
        }
    }
}
