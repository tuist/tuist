struct AutogeneratedWorkspaceSchemeWorkspaceMapperTests {
    private let buildInsightsActionMapper: MockBuildInsightsActionMapping
    private let testInsightsActionMapper: MockTestInsightsActionMapping
    init() {
        buildInsightsActionMapper = MockBuildInsightsActionMapping()
        testInsightsActionMapper = MockTestInsightsActionMapping()

        given(buildInsightsActionMapper)
        .map(.any, target: .any, buildInsightsDisabled: .any)
        .willProduce { buildAction, _, _ in buildAction }

        given(testInsightsActionMapper)
        .map(.any, target: .any, testInsightsDisabled: .any)
        .willProduce { testAction, _, _ in testAction }
    }

    @Test(.inTemporaryDirectory)
    func test_map() async throws {
        // Given
        let subject = AutogeneratedWorkspaceSchemeGraphMapper(
            forceWorkspaceSchemes: false,
            buildInsightsDisabled: true,
            testInsightsDisabled: true,
            buildInsightsActionMapper: buildInsightsActionMapper,
            testInsightsActionMapper: testInsightsActionMapper
        )
        let targetA = Target.test(
            name: "A"
        )
        let targetATests = Target.test(
            name: "ATests",
            product: .unitTests,
            dependencies: [.target(name: "A")]
        )

        let projectPath = try #require(FileSystem.temporaryTestDirectory)
        let project = Project.test(
            path: projectPath,
            targets: [
                targetA,
                targetATests,
            ]
        )

        let targetB = Target.test(
            name: "B"
        )
        let targetBTests = Target.test(
            name: "BTests",
            product: .unitTests,
            dependencies: [.target(name: "B")]
        )

        let projectBPath = try #require(FileSystem.temporaryTestDirectory).appending(component: "ProjectB")
        let projectB = Project.test(
            path: projectBPath,
            targets: [
                targetB,
                targetBTests,
            ]
        )

        let workspace = Workspace.test(
            name: "A",
            projects: [
                project.path,
                projectB.path,
            ]
        )

        let graph = Graph.test(
            workspace: workspace,
            projects: [
                projectPath: project,
                projectBPath: projectB,
            ]
        )

        // When
        let (got, sideEffects, _) = try await subject.map(
            graph: graph,
            environment: MapperEnvironment()
        )

        // Then
        #expect(sideEffects.isEmpty)
        let schemes = got.workspace.schemes

        #expect(schemes.count == 1)
        let scheme = try #require(schemes.first)
        #expect(scheme.shared)
        #expect(scheme.name == "A-Workspace")
        #expect(Set(scheme.buildAction.map(\.targets) ?? []) == Set([
                TargetReference(
                    projectPath: projectBPath,
                    name: targetB.name
                ),
                TargetReference(
                    projectPath: projectPath,
                    name: targetA.name
                ),
                TargetReference(
                    projectPath: projectPath,
                    name: targetATests.name
                ),
                TargetReference(
                    projectPath: projectBPath,
                    name: targetBTests.name
                ),
            ]))
        #expect(Set(scheme.testAction.map(\.targets) ?? []) == Set([
                TestableTarget(
                    target: TargetReference(
                        projectPath: projectPath,
                        name: targetATests.name
                    ),
                    parallelization: .none
                ),
                TestableTarget(
                    target: TargetReference(
                        projectPath: projectBPath,
                        name: targetBTests.name
                    ),
                    parallelization: .none
                ),
            ]))
        #expect(!try #require(scheme.testAction?.coverage))
    }

