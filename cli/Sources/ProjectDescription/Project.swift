/// A project representation.
///
/// A project manifest needs to be defined in a `Project.swift` manifest file.
/// Manifests need to import the framework ProjectDescription which contains all
/// the classes and enums that are available for you to describe your projects.
///
/// The snippet below shows an example project manifest:
///
/// ```swift
/// import ProjectDescription
///
/// let project = Project(
///     name: "MyProject",
///     organizationName: "MyOrg",
///     targets: [
///         .target(
///             name: "App",
///             destinations: .iOS,
///             product: .app,
///             bundleId: "dev.tuist.App",
///             infoPlist: "Config/App-Info.plist",
///             sources: ["Sources/**"],
///             resources: [
///                 "Resources/**",
///                 .folderReference(path: "Stubs"),
///                 .folderReference(path: "ODR", tags: ["odr_tag"])
///             ],
///             headers: .headers(
///                 public: ["Sources/public/A/**", "Sources/public/B/**"],
///                 private: "Sources/private/**",
///                 project: ["Sources/project/A/**", "Sources/project/B/**"]
///             ),
///             dependencies: [
///                 .project(target: "Framework1", path: "../Framework1"),
///                 .project(target: "Framework2", path: "../Framework2")
///             ]
///         )
///     ],
///     schemes: [
///         Scheme(
///             name: "App-Debug",
///             shared: true,
///             buildAction: .buildAction(targets: ["App"]),
///             testAction: .targets(["AppTests"]),
///             runAction: .runAction(executable: "App")
///         ),
///         Scheme(
///             name: "App-Release",
///             shared: true,
///             buildAction: .buildAction(targets: ["App"]),
///             runAction: .runAction(executable: "App")
///         )
///     ],
///     additionalFiles: [
///         "Dangerfile.swift",
///         "Documentation/**",
///         .folderReference(path: "Website")
///     ]
/// )
/// ```
public struct Project: Codable, Equatable, Sendable {
    private enum CodingKeys: String, CodingKey {
        case name
        case organizationName
        case classPrefix
        case options
        case packages
        case packageDependencies
        case targets
        case schemes
        case settings
        case fileHeaderTemplate
        case additionalFiles
        case resourceSynthesizers
    }

    /// The name of the project. Also, the file name of the generated Xcode project.
    public let name: String
    /// The name of the organization used by Xcode as copyright.
    public let organizationName: String?
    /// The prefix for class files Xcode generates when you create a project or class file.
    public let classPrefix: String?
    /// The project options.
    public let options: Options
    private let packageDependenciesStorage: [Package.Dependency]

    /// The Swift packages used by the project, represented using the legacy package model.
    public var packages: [Package] {
        packageDependenciesStorage.map(\.package)
    }

    /// Swift package dependencies using the Swift Package Manager-aligned model.
    @_spi(TuistLoader) public var packageDependencies: [Package.Dependency] {
        packageDependenciesStorage
    }

    /// The targets of the project.
    public let targets: [Target]
    /// The custom schemes for the project. Default schemes for each target are generated by default.
    public let schemes: [Scheme]
    /// The build settings and configuration for the project.
    public let settings: Settings?
    /// The custom file header template for Xcode built-in file templates.
    public let fileHeaderTemplate: FileHeaderTemplate?
    /// The additional files for the project. For target's additional files, see ``Target/additionalFiles``.
    public let additionalFiles: [FileElement]
    /// The resource synthesizers for the project to generate accessors for resources.
    public let resourceSynthesizers: [ResourceSynthesizer]

    /// Creates a project using Swift Package Manager-aligned package dependencies.
    ///
    /// Package traits require Xcode 26.4 or later.
    public init(
        name: String,
        organizationName: String? = nil,
        classPrefix: String? = nil,
        options: Options = .options(),
        packages: [Package.Dependency] = [],
        settings: Settings? = nil,
        targets: [Target] = [],
        schemes: [Scheme] = [],
        fileHeaderTemplate: FileHeaderTemplate? = nil,
        additionalFiles: [FileElement] = [],
        resourceSynthesizers: [ResourceSynthesizer] = .default
    ) {
        self.init(
            name: name,
            organizationName: organizationName,
            classPrefix: classPrefix,
            options: options,
            packageDependencies: packages,
            settings: settings,
            targets: targets,
            schemes: schemes,
            fileHeaderTemplate: fileHeaderTemplate,
            additionalFiles: additionalFiles,
            resourceSynthesizers: resourceSynthesizers
        )
    }

    /// Creates a project using the legacy package model.
    @available(*, deprecated, message: "Use Package.Dependency values in the packages parameter instead.")
    @_disfavoredOverload
    public init(
        name: String,
        organizationName: String? = nil,
        classPrefix: String? = nil,
        options: Options = .options(),
        packages: [Package],
        settings: Settings? = nil,
        targets: [Target] = [],
        schemes: [Scheme] = [],
        fileHeaderTemplate: FileHeaderTemplate? = nil,
        additionalFiles: [FileElement] = [],
        resourceSynthesizers: [ResourceSynthesizer] = .default
    ) {
        self.init(
            name: name,
            organizationName: organizationName,
            classPrefix: classPrefix,
            options: options,
            packageDependencies: packages.map { Package.Dependency(legacyPackage: $0) },
            settings: settings,
            targets: targets,
            schemes: schemes,
            fileHeaderTemplate: fileHeaderTemplate,
            additionalFiles: additionalFiles,
            resourceSynthesizers: resourceSynthesizers
        )
    }

    private init(
        name: String,
        organizationName: String? = nil,
        classPrefix: String? = nil,
        options: Options = .options(),
        packageDependencies: [Package.Dependency],
        settings: Settings? = nil,
        targets: [Target] = [],
        schemes: [Scheme] = [],
        fileHeaderTemplate: FileHeaderTemplate? = nil,
        additionalFiles: [FileElement] = [],
        resourceSynthesizers: [ResourceSynthesizer] = .default
    ) {
        self.name = name
        self.organizationName = organizationName
        self.classPrefix = classPrefix
        self.options = options
        packageDependenciesStorage = packageDependencies
        self.targets = targets
        self.schemes = schemes
        self.settings = settings
        self.additionalFiles = additionalFiles
        self.fileHeaderTemplate = fileHeaderTemplate
        self.resourceSynthesizers = resourceSynthesizers
        dumpIfNeeded(self)
    }

    public init(from decoder: Decoder) throws {
        let container = try decoder.container(keyedBy: CodingKeys.self)
        name = try container.decode(String.self, forKey: .name)
        organizationName = try container.decodeIfPresent(String.self, forKey: .organizationName)
        classPrefix = try container.decodeIfPresent(String.self, forKey: .classPrefix)
        options = try container.decode(Options.self, forKey: .options)
        targets = try container.decode([Target].self, forKey: .targets)
        schemes = try container.decode([Scheme].self, forKey: .schemes)
        settings = try container.decodeIfPresent(Settings.self, forKey: .settings)
        fileHeaderTemplate = try container.decodeIfPresent(FileHeaderTemplate.self, forKey: .fileHeaderTemplate)
        additionalFiles = try container.decode([FileElement].self, forKey: .additionalFiles)
        resourceSynthesizers = try container.decode([ResourceSynthesizer].self, forKey: .resourceSynthesizers)

        let packages = try container.decode([Package].self, forKey: .packages)
        if let dependencies = try container.decodeIfPresent([Package.Dependency].self, forKey: .packageDependencies) {
            guard dependencies.map(\.package) == packages else {
                throw DecodingError.dataCorruptedError(
                    forKey: .packageDependencies,
                    in: container,
                    debugDescription: "Package dependencies do not match the legacy package representation"
                )
            }
            packageDependenciesStorage = dependencies
        } else {
            packageDependenciesStorage = packages.map { Package.Dependency(legacyPackage: $0) }
        }
    }

    public func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encode(name, forKey: .name)
        try container.encodeIfPresent(organizationName, forKey: .organizationName)
        try container.encodeIfPresent(classPrefix, forKey: .classPrefix)
        try container.encode(options, forKey: .options)
        try container.encode(packages, forKey: .packages)
        try container.encode(packageDependenciesStorage, forKey: .packageDependencies)
        try container.encode(targets, forKey: .targets)
        try container.encode(schemes, forKey: .schemes)
        try container.encodeIfPresent(settings, forKey: .settings)
        try container.encodeIfPresent(fileHeaderTemplate, forKey: .fileHeaderTemplate)
        try container.encode(additionalFiles, forKey: .additionalFiles)
        try container.encode(resourceSynthesizers, forKey: .resourceSynthesizers)
    }

    /// The project contains targets that depend on external dependencies
    public var containsExternalDependencies: Bool {
        targets.contains(where: { target in
            target.dependencies.contains(where: {
                if case .external = $0 {
                    return true
                } else {
                    return false
                }
            })
        })
    }
}
