import Foundation
import Path

/// A execution action
public struct ExecutionAction: Equatable, Codable, Sendable {
    // MARK: - Attributes

    /// Name of a script.
    public let title: String
    /// An inline shell script.
    public let scriptText: String
    /// Name of the build or test target that will provide the action's build settings.
    public let target: TargetReference?
    /// The path to the shell which shall execute this script. if it is nil, Xcode will use default value.
    public let shellPath: String?

    public let showEnvVarsInLog: Bool

    /// True when the action was added implicitly by Tuist (e.g. the autogenerated insight
    /// post-actions). Such an action's `target` only exists to expose `environmentBuildable`
    /// build settings to the script; the script does not depend on that specific target being
    /// in the focused graph, so the focus mapper is free to rewrite the target to a surviving
    /// one rather than preserve the original target through focus.
    public let isImplicit: Bool

    // MARK: - Init

    public init(
        title: String,
        scriptText: String,
        target: TargetReference?,
        shellPath: String?,
        showEnvVarsInLog: Bool = true,
        isImplicit: Bool = false
    ) {
        self.title = title
        self.scriptText = scriptText
        self.target = target
        self.shellPath = shellPath
        self.showEnvVarsInLog = showEnvVarsInLog
        self.isImplicit = isImplicit
    }

    public init(from decoder: Decoder) throws {
        let container = try decoder.container(keyedBy: CodingKeys.self)
        title = try container.decode(String.self, forKey: .title)
        scriptText = try container.decode(String.self, forKey: .scriptText)
        target = try container.decodeIfPresent(TargetReference.self, forKey: .target)
        shellPath = try container.decodeIfPresent(String.self, forKey: .shellPath)
        showEnvVarsInLog = try container.decode(Bool.self, forKey: .showEnvVarsInLog)
        isImplicit = try container.decodeIfPresent(Bool.self, forKey: .isImplicit) ?? false
    }
}
