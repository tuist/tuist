import ArgumentParser
import Foundation
import TuistEnvKey
import TuistNooraExtension

public struct BazelCredentialHelperCommand: AsyncParsableCommand, NooraReadyCommand {
    public var jsonThroughNoora: Bool { true }

    public init() {}

    public static var configuration: CommandConfiguration {
        CommandConfiguration(
            commandName: "credential-helper",
            _superCommandName: "bazel",
            abstract: "Provide credentials to Bazel following the credential helper specification.",
            discussion: """
            Implements https://github.com/EngFlow/credential-helper-spec/blob/main/spec.md. Bazel invokes
            this command through the tuist-bazel-credential-helper script generated by 'tuist bazel setup'
            and expects a JSON payload with the credential headers on the standard output.
            """
        )
    }

    @Argument(
        help: "The credential helper command to run. Only 'get' is supported."
    )
    var command: String

    @Option(
        name: [.customShort("p"), .long],
        help: "The path to the directory containing the Tuist project.",
        envKey: .bazelCredentialHelperPath
    )
    var path: String?

    public func run() async throws {
        try await BazelCredentialHelperCommandService().run(
            helperCommand: command,
            directory: path
        )
    }
}
