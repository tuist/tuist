defmodule TuistWeb.API.RunsController do
  alias Tuist.CommandEvents
  alias TuistWeb.API.Schemas.Error
  alias TuistWeb.API.Schemas.Run
  alias TuistWeb.API.Schemas.Runs.Build
  alias TuistWeb.Authentication
  alias Tuist.Runs
  alias OpenApiSpex.Schema
  use OpenApiSpex.ControllerSpecs
  use TuistWeb, :controller

  plug(OpenApiSpex.Plug.CastAndValidate,
    json_render_error_v2: true,
    render_error: TuistWeb.RenderAPIErrorPlug
  )

  plug(TuistWeb.API.EnsureProjectPresencePlug)
  plug(TuistWeb.API.Authorization.AuthorizationPlug, :run)

  tags ["Runs"]

  operation(:index,
    summary: "List runs associated with a given project.",
    operation_id: "listRuns",
    parameters: [
      account_handle: [
        in: :path,
        type: :string,
        required: true,
        description: "The handle of the account."
      ],
      project_handle: [
        in: :path,
        type: :string,
        required: true,
        description: "The handle of the project."
      ],
      name: [
        in: :query,
        type: :string,
        description: "The name of the run."
      ],
      git_ref: [
        in: :query,
        type: :string,
        description: "The git ref of the run."
      ],
      git_branch: [
        in: :query,
        type: :string,
        description: "The git branch of the run."
      ],
      git_commit_sha: [
        in: :query,
        type: :string,
        description: "The git commit SHA of the run."
      ],
      page_size: [
        in: :query,
        type: %Schema{
          title: "RunsIndexPageSize",
          description: "The maximum number of runs to return in a single page.",
          type: :integer,
          default: 20,
          minimum: 1,
          maximum: 100
        }
      ],
      page: [
        in: :query,
        type: %Schema{
          title: "RunsIndexPage",
          description: "The page number to return.",
          type: :integer,
          default: 1,
          minimum: 1
        }
      ]
    ],
    responses: %{
      ok:
        {"List of runs", "application/json",
         %Schema{
           type: :object,
           properties: %{
             runs: %Schema{
               type: :array,
               items: Run
             }
           },
           required: [:runs]
         }},
      forbidden: {"You don't have permission to access this resource", "application/json", Error}
    }
  )

  def index(
        %{
          assigns: %{selected_project: selected_project},
          params:
            %{
              page_size: page_size,
              page: page
            } = params
        } =
          conn,
        _params
      ) do
    filters =
      [
        %{field: :project_id, op: :==, value: selected_project.id}
      ] ++ filters_from_params(params)

    {command_events, _meta} =
      CommandEvents.list_command_events(%{
        page: page,
        page_size: page_size,
        filters: filters,
        order_by: [:created_at],
        order_directions: [:desc]
      })

    conn
    |> json(%{
      runs:
        command_events
        |> Enum.map(fn event ->
          event
          |> Map.take([
            :id,
            :name,
            :duration,
            :subcommand,
            :command_arguments,
            :tuist_version,
            :swift_version,
            :macos_version,
            :status,
            :git_ref,
            :git_commit_sha,
            :git_branch,
            :cacheable_targets,
            :local_cache_target_hits,
            :remote_cache_target_hits,
            :test_targets,
            :local_test_target_hits,
            :remote_test_target_hits,
            :preview_id
          ])
          |> Map.put(
            :url,
            ~p"/#{selected_project.account.name}/#{selected_project.name}/runs/#{event.id}"
          )
        end)
    })
  end

  operation(:create,
    summary: "Create a new build.",
    parameters: [
      account_handle: [
        in: :path,
        type: :string,
        required: true,
        description: "The handle of the project's account."
      ],
      project_handle: [
        in: :path,
        type: :string,
        required: true,
        description: "The handle of the project to create a build for."
      ]
    ],
    operation_id: "createRun",
    request_body:
      {"Run params", "application/json",
       %Schema{
         title: "RunParams",
         description: "Parameters to create a single run.",
         oneOf: [
           %Schema{
             title: "BuildRun",
             type: :object,
             properties: %{
               type: %Schema{
                 type: :string,
                 enum: ["build"],
                 description: "The type of the run, which is 'build' in this case."
               },
               id: %Schema{
                 description: "UUID of a run generated by the system.",
                 type: :string
               },
               duration: %Schema{
                 description: "Duration of the run in milliseconds.",
                 type: :integer
               },
               macos_version: %Schema{
                 type: :string,
                 description: "The version of macOS used during the run."
               },
               xcode_version: %Schema{
                 type: :string,
                 description: "The version of Xcode used during the run."
               },
               is_ci: %Schema{
                 type: :boolean,
                 description:
                   "Indicates if the run was executed on a Continuous Integration (CI) system."
               },
               model_identifier: %Schema{
                 type: :string,
                 description:
                   "Identifier for the model where the run was executed, such as MacBookAir10,1."
               },
               scheme: %Schema{
                 type: :string,
                 description: "The scheme used for the build."
               }
             },
             required: [
               :id,
               :duration,
               :is_ci
             ]
           }
         ]
       }},
    responses: %{
      ok: {
        "The created build",
        "application/json",
        %Schema{
          oneOf: [Build]
        }
      },
      unauthorized: {"You need to be authenticated to create a run", "application/json", Error},
      forbidden:
        {"The authenticated subject is not authorized to perform this action", "application/json",
         Error},
      not_found: {"The project doesn't exist", "application/json", Error},
      bad_request: {"The request parameters are invalid", "application/json", Error}
    }
  )

  def create(
        %{
          assigns: %{selected_project: selected_project},
          body_params: body_params
        } = conn,
        _params
      ) do
    build_params =
      body_params
      |> Map.put(:project, selected_project)
      |> Map.put(:account, Authentication.current_user(conn).account)

    case get_or_create_build(build_params) do
      {:ok, build} ->
        conn
        |> put_status(:ok)
        |> json(%{
          id: build.id,
          duration: build.duration,
          project_id: build.project_id
        })

      {:error, _changeset} ->
        conn |> put_status(:bad_request) |> json(%{message: "The request parameters are invalid"})
    end
  end

  defp get_or_create_build(params) do
    case Runs.get_build(params.id) do
      %Runs.Build{} = build ->
        {:ok, build}

      nil ->
        Runs.create_build(%{
          id: params.id,
          duration: params.duration,
          macos_version: params |> Map.get(:macos_version),
          xcode_version: params |> Map.get(:xcode_version),
          is_ci: params |> Map.get(:is_ci),
          model_identifier: params |> Map.get(:model_identifier),
          scheme: params |> Map.get(:scheme),
          project_id: params.project.id,
          account_id: params.account.id
        })
    end
  end

  defp filters_from_params(params) do
    [:name, :git_ref, :git_branch, :git_commit_sha]
    |> Enum.map(&%{field: &1, op: :==, value: Map.get(params, &1)})
    |> Enum.filter(&(&1.value != nil))
  end
end
