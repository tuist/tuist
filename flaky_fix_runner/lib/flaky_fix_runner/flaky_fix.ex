defmodule FlakyFixRunner.FlakyFix do
  @moduledoc false

  require Logger

  @default_tools %{
    git: "/usr/bin/git",
    gh: "/opt/homebrew/bin/gh",
    opencode: "/opt/homebrew/bin/opencode",
    tuist: "/opt/homebrew/bin/tuist"
  }

  def process(%{
        "job_id" => job_id,
        "test_case_id" => test_case_id,
        "test_case_url" => test_case_url,
        "repository_full_handle" => repository_full_handle,
        "repository_url" => repository_url,
        "base_branch" => base_branch,
        "callback_url" => callback_url
      }) do
    Logger.info(
      "Starting flaky fix job #{job_id} for #{repository_full_handle} test case #{test_case_id}"
    )

    branch = branch_name(test_case_id)
    notify(callback_url, %{job_id: job_id, status: "running", branch: branch})

    with repo_dir <- ensure_repo_checkout(repository_full_handle),
         :ok <- checkout_branch(repo_dir, base_branch, branch),
         :ok <- run_opencode(repo_dir, test_case_id, test_case_url, repository_url, branch),
         :ok <- maybe_commit_changes(repo_dir, test_case_id, base_branch),
         pr_url <- create_draft_pr(repo_dir, test_case_id, base_branch, branch) do
      notify(callback_url, %{job_id: job_id, status: "pr_opened", branch: branch, pr_url: pr_url})
      :ok
    else
      {:error, message} ->
        Logger.warning("Flaky fix job #{job_id} failed: #{message}")

        notify(callback_url, %{job_id: job_id, status: "failed", branch: branch, message: message})

        :error
    end
  end

  defp ensure_repo_checkout(repository_full_handle) do
    repo_dir = repo_dir(repository_full_handle)
    File.mkdir_p!(Path.dirname(repo_dir))

    git = tool_path(:git)
    gh = tool_path(:gh)

    if File.dir?(repo_dir) do
      run_cmd!(git, ["fetch", "--all", "--prune"], cd: repo_dir)
    else
      run_cmd!(gh, ["repo", "clone", repository_full_handle, repo_dir])
    end

    repo_dir
  end

  defp checkout_branch(repo_dir, base_branch, branch) do
    git = tool_path(:git)

    run_cmd!(git, ["checkout", base_branch], cd: repo_dir)
    run_cmd!(git, ["pull", "--ff-only", "origin", base_branch], cd: repo_dir)
    run_cmd!(git, ["checkout", "-B", branch, "origin/#{base_branch}"], cd: repo_dir)

    :ok
  rescue
    error -> {:error, Exception.message(error)}
  end

  defp run_opencode(repo_dir, test_case_id, test_case_url, repository_url, branch) do
    opencode = tool_path(:opencode)
    prompt = opencode_prompt(test_case_id, test_case_url, repository_url, branch)

    run_cmd!(opencode, ["run", "--dir", repo_dir, prompt], cd: repo_dir)
    :ok
  rescue
    error -> {:error, Exception.message(error)}
  end

  defp maybe_commit_changes(repo_dir, test_case_id, base_branch) do
    git = tool_path(:git)
    status = run_cmd!(git, ["status", "--porcelain"], cd: repo_dir)

    if String.trim(status) != "" do
      run_cmd!(git, ["add", "-A"], cd: repo_dir)

      run_cmd!(
        git,
        ["commit", "-m", "fix: resolve flaky test #{String.slice(test_case_id, 0, 8)}"],
        cd: repo_dir
      )
    end

    ahead_count =
      git
      |> run_cmd!(["rev-list", "--count", "origin/#{base_branch}..HEAD"], cd: repo_dir)
      |> String.trim()

    if ahead_count == "0" do
      {:error, "OpenCode finished without creating a commit"}
    else
      :ok
    end
  rescue
    error -> {:error, Exception.message(error)}
  end

  defp create_draft_pr(repo_dir, test_case_id, base_branch, branch) do
    git = tool_path(:git)
    gh = tool_path(:gh)

    run_cmd!(git, ["push", "-u", "origin", branch], cd: repo_dir)

    gh
    |> run_cmd!(
      [
        "pr",
        "create",
        "--draft",
        "--base",
        base_branch,
        "--head",
        branch,
        "--title",
        "Fix flaky test #{String.slice(test_case_id, 0, 8)}",
        "--body",
        "Automated flaky test fix generated by Flaky Fix Runner."
      ],
      cd: repo_dir
    )
    |> String.trim()
  rescue
    error -> {:error, Exception.message(error)}
  end

  defp notify(callback_url, payload) do
    webhook_secret = Application.get_env(:flaky_fix_runner, :webhook_secret)
    json_body = Jason.encode!(payload)

    signature =
      :hmac
      |> :crypto.mac(:sha256, webhook_secret, json_body)
      |> Base.encode16(case: :lower)

    Req.post!(callback_url,
      body: json_body,
      headers: [
        {"content-type", "application/json"},
        {"x-webhook-signature", signature}
      ]
    )
  end

  defp opencode_prompt(test_case_id, test_case_url, repository_url, branch) do
    skill_text =
      case skill_path() do
        nil -> ""
        path -> "\n\nCanonical skill:\n\n" <> File.read!(path)
      end

    """
    Fix the flaky test identified by `#{test_case_id}`.

    Context:
    - Test case URL: #{test_case_url}
    - Repository: #{repository_url}
    - Working branch: #{branch}

    Workflow requirements:
    - use the Tuist CLI to inspect the flaky test and flaky runs
    - edit the local checkout only for the flaky test fix
    - run the relevant test repeatedly until it stops failing intermittently
    - do not create the PR yourself; this service handles git push and draft PR creation
    - do not leave the repository with unstaged changes when you are done
    #{skill_text}
    """
  end

  defp skill_path do
    configured = Application.get_env(:flaky_fix_runner, :skill_path)

    cond do
      is_binary(configured) and File.exists?(configured) ->
        configured

      true ->
        guessed = Path.expand("../skills/skills/fix-flaky-tests/SKILL.md", File.cwd!())
        if File.exists?(guessed), do: guessed
    end
  end

  defp repo_dir(repository_full_handle) do
    base_dir =
      Application.get_env(
        :flaky_fix_runner,
        :repo_base_dir,
        Path.join(System.user_home!(), "tuist-flaky-fixes")
      )

    Path.join(base_dir, repository_full_handle)
  end

  defp branch_name(test_case_id) do
    suffix =
      test_case_id
      |> String.downcase()
      |> String.replace(~r/[^a-z0-9]+/, "-")
      |> String.trim("-")

    "auto/fix-flaky-test/#{suffix}"
  end

  defp run_cmd!(executable, args, opts \\ []) do
    cd = Keyword.get(opts, :cd)

    {output, exit_code} =
      System.cmd(executable, args,
        cd: cd,
        stderr_to_stdout: true,
        env: [{"PATH", path_env()}]
      )

    if exit_code == 0 do
      output
    else
      raise RuntimeError, "command failed: #{executable} #{Enum.join(args, " ")}\n#{output}"
    end
  end

  defp tool_path(tool) do
    fallback = Map.fetch!(@default_tools, tool)
    System.find_executable(Atom.to_string(tool)) || fallback
  end

  defp path_env do
    [
      "/opt/homebrew/bin",
      "/usr/local/bin",
      System.get_env("PATH") || ""
    ]
    |> Enum.join(":")
  end
end
